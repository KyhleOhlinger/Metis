function isTrackerActive(status: TrackerStatus): boolean {
  return status === "Complete" || status === "Coming Up";
}

function isLongWeekendHoliday(dateIso: string): boolean {
  const d = parseIsoDateLocal(dateIso);
  if (!d) return false;
  const day = d.getDay();
  return day === 1 || day === 5; // Monday or Friday
}

type TrackerOverride = {
  status: TaskStatus;
  label: string;
  sourceType: "holiday" | "pto" | "conference" | "trip";
  sourceId: string;
};

type OfficeTripBanner = {
  label: string;
  sourceId: string;
};

function buildTrackerOverrides(tracker: TrackerData): Map<string, TrackerOverride> {
  const out = new Map<string, TrackerOverride>();
  for (const row of tracker.public_holidays) {
    if (!isTrackerActive(row.status)) continue;
    const label = row.name || SPECIAL_LABELS.holiday;
    out.set(row.date, {
      status: "holiday",
      label: isLongWeekendHoliday(row.date) ? `${label} · Long Weekend` : label,
      sourceType: "holiday",
      sourceId: row.id,
    });
  }
  for (const row of tracker.pto) {
    if (!isTrackerActive(row.status)) continue;
    eachDateInclusive(row.startDate, row.endDate, (d) => {
      out.set(toIsoDate(d), {
        status: "pto",
        label: row.description ? `PTO · ${row.description}` : SPECIAL_LABELS.pto,
        sourceType: "pto",
        sourceId: row.id,
      });
    });
  }
  for (const row of tracker.conferences) {
    if (!isTrackerActive(row.status) && row.activity !== "Booked") continue;
    eachDateInclusive(row.startDate, row.endDate, (d) => {
      out.set(toIsoDate(d), {
        status: "offsite",
        label: row.eventName || SPECIAL_LABELS.offsite,
        sourceType: "conference",
        sourceId: row.id,
      });
    });
  }
  return out;
}

function buildOfficeTripBanners(tracker: TrackerData): Map<string, OfficeTripBanner> {
  const out = new Map<string, OfficeTripBanner>();
  for (const row of tracker.office_trips) {
    if (!isTrackerActive(row.status) && row.activity !== "Booked") continue;
    eachDateInclusive(row.startDate, row.endDate, (d) => {
      out.set(toIsoDate(d), {
        label: row.tripName || "Office Trip",
        sourceId: row.id,
      });
    });
  }
  return out;
}

function getEntry(
  manifest: TaskManifest,
  monday: Date,
  day: DayName,
): DayEntry {
  const wk = weekKey(monday);
  return monthEntryFor(manifest, monday).daily_logs[wk]?.[day] ?? { status: "work", planned: "", did: "" };
}

function setEntry(
  manifest: TaskManifest,
  monday: Date,
  day: DayName,
  entry: DayEntry,
): TaskManifest {
  const year = String(monday.getFullYear());
  const month = monthName(monday);
  const wk = weekKey(monday);
  const yearEntry = getYearEntry(manifest, year);
  const monthEntry = yearEntry[month] ?? makeEmptyMonthEntry();
  return {
    ...manifest,
    [year]: {
      ...yearEntry,
      [month]: {
        ...monthEntry,
        daily_logs: {
          ...monthEntry.daily_logs,
          [wk]: {
            ...(monthEntry.daily_logs[wk] ?? {}),
            [day]: entry,
          },
        },
      },
    },
  };
}

